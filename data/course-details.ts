function makeSection(
  id: string,
  title: string,
  lessons: string[],
): CourseCatalogSection {
  return {
    id,
    title,
    lessonCount: '1节',
    totalHours: '2小时',
    lessons: lessons.map((lesson, index) => ({
      title: lesson,
      duration: ['35:00', '40:00', '45:00'][index] ?? '38:00',
      preview: index === 0,
    })),
  }
}

function makeSeoSection(
  title: string,
  paragraphs: string[],
): CourseSeoSection {
  return {
    title,
    paragraphs,
  }
}

