type ArticleSeed = Omit<InsightArticle, 'publishedAt' | 'sections'> & {
  problem: string
  why: string
  approach: string
  closing: string
  bullets?: string[]
}

const DEFAULT_DATE = '2026-03-28'

function toArticle(seed: ArticleSeed): InsightArticle {
  return {
    slug: seed.slug,
    title: seed.title,
    excerpt: seed.excerpt,
    description: seed.description,
    category: seed.category,
    tags: seed.tags,
    publishedAt: DEFAULT_DATE,
    readTime: seed.readTime,
    featured: seed.featured,
    relatedCourseSlugs: seed.relatedCourseSlugs,
    sections: [
      {
        title: '为什么这个问题值得先想清楚',
        paragraphs: [seed.problem, seed.why],
        bullets: seed.bullets,
      },
      {
        title: '更稳的处理方式是什么',
        paragraphs: [seed.approach, seed.closing],
      },
    ],
    keyTakeaways: seed.keyTakeaways,
  }
}

